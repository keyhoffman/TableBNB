import Foundation
import UIKit

// MARK: - UIColor Extension

extension UIColor {
    static func darkGray() -> UIColor {
        return UIColor(red:0.23, green:0.23, blue:0.27, alpha:1.00)
    }
    
    static func whiteGrayScale08() -> UIColor {
        return UIColor(white: 0.8, alpha: 1)
    }
}
